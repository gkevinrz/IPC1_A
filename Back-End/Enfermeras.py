class Enfermeras:
    def __init__(self,NombreEnf,ApellidoEnf,FechaEnf,GeneroEnf,UserEnf,PasswordEnf,TelefonoEnf):
        self.NombreEnf=NombreEnf
        self.ApellidoEnf=ApellidoEnf
        self.FechaEnf=FechaEnf
        self.GeneroEnf=GeneroEnf
        self.UserEnf=UserEnf
        self.PasswordEnf=PasswordEnf
        self.TelefonoEnf=TelefonoEnf

    def getNombreEnfermera(self):
        return self.NombreEnf

    def getApellidoEnfermera(self):
        return self.ApellidoEnf

    def getFechaEnfermera(self):
        return self.FechaEnf
    
    def getGeneroEnfermera(self):
        return self.GeneroEnf

    def getUserEnfermera(self):
        return self.UserEnf
    
    def getPasswordEnfermera(self):
        return self.PasswordEnf

    def geTelefonoEnfermera(self):
        return self.TelefonoEnf




